package entidades;

public class Departamento {
	private String nome;
	
	// Construtores
	public Departamento() {
	}

	public Departamento(String nome) {
		this.nome = nome;
	}
	// Getters e Setters

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
		
}
